XiHadamardLeanEmitRefusal xi_hadamard_lean_emit_refusal(const Heat::XiHadamardReport& report) {
    if (!report.proof_chain_closed) return XiHadamardLeanEmitRefusal::ProofChainOpen;
    if (!report.non_circular_architecture_ok || report.proof_graph.circular_logic_detected)
        return XiHadamardLeanEmitRefusal::CircularGraph;
    if (!xi_hadamard_report_bounds_ok(report)) return XiHadamardLeanEmitRefusal::BoundsExceedTolerance;
    return XiHadamardLeanEmitRefusal::None;
}

bool emit_xi_hadamard_lean_artifacts(const std::string& cert_lean_path,
                                     const std::string& canonical_lean_path,
                                     const std::string& rh_closure_lean_path,
                                     const Heat::XiHadamardReport& report,
                                     XiHadamardLeanEmitOptions opts) {
    const auto refusal = xi_hadamard_lean_emit_refusal(report);
    if (refusal != XiHadamardLeanEmitRefusal::None) {
        if (!opts.quiet_refusal) {
            switch (refusal) {
                case XiHadamardLeanEmitRefusal::ProofChainOpen:
                    std::cerr << "AnaLeanEmitter: proof_chain_closed=false; refusing Lean emission\n";
                    break;
                case XiHadamardLeanEmitRefusal::CircularGraph:
                    std::cerr << "AnaLeanEmitter: circular proof graph; refusing Lean emission\n";
                    break;
                case XiHadamardLeanEmitRefusal::BoundsExceedTolerance:
                    std::cerr << "AnaLeanEmitter: audit bounds exceed MRS tolerance; refusing Lean emission\n";
                    break;
                default:
                    break;
            }
        }
        return false;
    }
    const std::string cert_body = emit_cert_lean(report);
    const std::string canonical_body = emit_canonical_product_lean(report);
    const std::string rh_body = emit_rh_closure_lean(report);
    const std::string bridge_path = sibling_lean_path(cert_lean_path, "MarshalGenusOneLogAnalyticBridge.lean");
    const std::string bounds_path = sibling_lean_path(cert_lean_path, "MarshalAnaVmGenusOneLogBounds.lean");
    const std::string bridge_body = emit_genus_one_log_analytic_bridge_lean(report);
    const std::string bounds_body = emit_marshal_genus_one_log_bounds_lean(report);
    if (!write_file(cert_lean_path, cert_body)) {
        std::cerr << "AnaLeanEmitter: failed to write " << cert_lean_path << "\n";
        return false;
    }
    if (!write_file(canonical_lean_path, canonical_body)) {
        std::cerr << "AnaLeanEmitter: failed to write " << canonical_lean_path << "\n";
        return false;
    }
    if (!rh_closure_lean_path.empty() && !write_file(rh_closure_lean_path, rh_body)) {
        std::cerr << "AnaLeanEmitter: failed to write " << rh_closure_lean_path << "\n";
        return false;
    }
    if (!write_file(bridge_path, bridge_body)) {
        std::cerr << "AnaLeanEmitter: failed to write " << bridge_path << "\n";
        return false;
    }
    if (!write_file(bounds_path, bounds_body)) {
        std::cerr << "AnaLeanEmitter: failed to write " << bounds_path << "\n";
        return false;
    }
    const auto validation = validate_xi_hadamard_lean_bundle(
        cert_body, canonical_body, rh_body, bridge_body, bounds_body, report);
    if (!validation.ok) {
        for (const auto& issue : validation.issues)
            std::cerr << "AnaLeanEmitter: codegen validation [" << issue.role << "]: "
                      << issue.message << "\n";
        return false;
    }
    std::cout << "AnaVM Lean cert: " << cert_lean_path << "\n";
    std::cout << "AnaVM Lean bridge: " << canonical_lean_path << "\n";
    std::cout << "AnaVM Lean genus-1 analytic bridge: " << bridge_path << "\n";
    std::cout << "AnaVM Lean genus-1 bounds: " << bounds_path << "\n";
    if (!rh_closure_lean_path.empty())
        std::cout << "AnaVM Lean RH closure: " << rh_closure_lean_path << "\n";
    return true;
}

