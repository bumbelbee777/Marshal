bool write_file(const std::string& path, const std::string& body) {
    std::ofstream out(path);
    if (!out) return false;
    out << body;
    return true;
}

struct BoundPin {
    std::string name;
    double value;
    std::string ub_name;
    double ub;
};

void emit_header(std::ostringstream& oss, const Heat::XiHadamardReport& r,
                 const char* module_note) {
    oss << "/-!\n";
    oss << "  AUTO-GENERATED by AnaVM AnaLeanEmitter — do not edit by hand.\n";
    oss << "  " << module_note << "\n";
    oss << "  program=" << r.program_id << " proof_chain_closed="
        << (r.proof_chain_closed ? "true" : "false") << " acyclic="
        << (r.non_circular_architecture_ok ? "true" : "false") << "\n";
    oss << "-/\n\n";
}

void emit_imports(std::ostringstream& oss) {
    oss << "import Analysis.CertifiedBounds\n";
    oss << "import Mathlib.Tactic\n\n";
}

void emit_real_def(std::ostringstream& oss, const std::string& name, double v) {
    oss << std::setprecision(17);
    oss << "noncomputable def " << name << " : ℝ := " << v << "\n\n";
}

void emit_nat_def(std::ostringstream& oss, const std::string& name, int v) {
    oss << "noncomputable def " << name << " : ℕ := " << v << "\n\n";
}

void emit_bool_def(std::ostringstream& oss, const std::string& name, bool v) {
    oss << "def " << name << " : Bool := " << (v ? "true" : "false") << "\n\n";
}

void emit_bound_theorem(std::ostringstream& oss, const BoundPin& pin) {
    oss << "theorem " << pin.name << "_bounded :\n";
    oss << "    " << pin.name << " < " << pin.ub_name << " := by\n";
    oss << "  unfold " << pin.name << " " << pin.ub_name << "\n";
    oss << "  norm_num\n\n";
}

